// Exercicio usando Switch
let estacao = 'Inverno'

switch (estacao){
    case 'Verão':
       console.log("Que calor hein?!")
    break;
    case "Inverno":
        console.log("Frio dms?!")
    break;
    case "Primavera":
        console.log("O momento em que as flores crescem")
    break;
    case "Outono":
        console.log("é a estação que as folhas caem")
    break;
    default:
    console.log("Ops,Estação inválida")
}

